class Speaker {
  final String speakerName;
  final String topic;
  final String imageUrl;

  final int likes;
  final int followers;
  final int following;

  final String shortDescription;
  final String emailId;
  final String mobileNo;

  Speaker(
      this.speakerName,
      this.topic,
      this.imageUrl,
      this.likes,
      this.followers,
      this.following,
      this.shortDescription,
      this.emailId,
      this.mobileNo);
}
